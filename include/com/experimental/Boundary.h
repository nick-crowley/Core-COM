#define		co_impl    return ::core::com::Boundary<std::remove_cvref_t<decltype(*this)>>{} = [&]

// o~=~-~=~-~=~-~=~-~=~-~=~-~=~-~=~-~o Constants & Enumerations o~-~=~-~=~-~=~-~=~-~=~-~=~-~=~-~=~o

